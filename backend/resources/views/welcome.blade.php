# Smart Booking Scheduler <br>
<br>
Appointment booking system built with Laravel and Vue.js. <br>
<br>
## Features Implemented <br>
<br>
Service selection<br>
calendar date picker.<br>  
Real-time available time slot display. <br> 
Admin panel for working hours management.<br>  
Prevention for double booking with locking the time slot, disable previous dates hide unavailable time slots.<br>
